    private void serialNumberTextFieldKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_serialNumberTextFieldKeyPressed

        int key = evt.getKeyCode();
        if ((key >= evt.VK_NUMPAD0 && key <= evt.VK_NUMPAD9) || (key >= evt.VK_0 && key <= evt.VK_9) || (key == evt.VK_BACK_SPACE)) {
            serialNumberTextField.setEditable(true);
        } else {
            serialNumberTextField.setEditable(false);
            JOptionPane.showMessageDialog(null, " Input format must be numbers");
            serialNumberTextField.setText("");
    }//GEN-LAST:event_serialNumberTextFieldKeyPressed

    }
