    private void addFlightBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addFlightBtnActionPerformed

        for (AirLiner a : Flightfleet.getFlightfleet()) {
            if (a.getName().equals(Name)) {
                System.out.println("IN loop " + a.getName());
                a.getF().setStartpoint(startPointTextField.getText());
                a.getF().setDestination(destinationTextField.getText());
                a.getF().setAvailableseats(Integer.parseInt(availableTextField.getText()));
                a.getAp().setModel(modelTextField.getText());
                a.getF().setSerialNumber(Integer.parseInt(serialNumberTextField.getText()));
                a.getAp().setTotalseats(Integer.parseInt(totalSeatsTextField.getText()));
                JOptionPane.showMessageDialog(null, "Flight Added");
                break;
            }
        }

        startPointTextField.setText("");
        destinationTextField.setText("");
        availableTextField.setText("");
        modelTextField.setText("");
        serialNumberTextField.setText("");
        totalSeatsTextField.setText("");

    }//GEN-LAST:event_addFlightBtnActionPerformed

