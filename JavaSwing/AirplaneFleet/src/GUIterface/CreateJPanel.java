    private void createBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createBtnActionPerformed
        // TODO add your handling code here:

        String dtime = departTextField.getText().trim();
        String manyear = manuTextField.getText();
        String seatss = availSeatsTextField.getText();
        int manuYear = Integer.parseInt((manuTextField.getText().trim()));
        int seats = Integer.parseInt(availSeatsTextField.getText().trim());
        String planetype = planeTypeTextField.getText().trim();
        String Model = modelNoTextField.getText().trim();
        String destination = destinationTextField.getText().trim();
        String startpoint = startTextField.getText().trim();
        String airliner = airlinerTextField.getText().trim();
        String serial = serialTextField.getText().trim();

        if (seatss.equals("") || manyear.equals("") || planetype.equals("") || Model.equals("") || destination.equals("") || startpoint.equals("") || airliner.equals("") || serial.equals("")) {
            JOptionPane.showMessageDialog(null, " Few Fields are left empty");
        } else {

            AirPlanes a = apf.addPlanes();

            if (licenseCheckBox.isSelected()) {
                String license = "valid";
                a.setLicense(license);
            }
            if (licenseExpiredCheckBox.isSelected()) {
                String license = "Expired";
                a.setLicense(license);
            }

            if (yesCheckBox.isSelected()) {
                String availability = "Yes";
                a.setAvailablity(availability);
            } else {
                a.setAvailablity("No");
            }
            if (noCheckBox.isSelected()) {
                String availability = "No";
                a.setAvailablity(availability);
            }
            a.setDeparture(dtime);
            a.setManufacturedyear(manuYear);
            a.setAvailableSeats(seats);
            a.setType(planetype);
            a.setModelNumber(Model);
            a.setDestination(destination);
            a.setStartPoint(startpoint);
            a.setAirliner(airliner);
            a.setSerialNumber(serial);

            JOptionPane.showMessageDialog(null, "The details for new plane has been entered");

            yesCheckBox.setSelected(false);
            departTextField.setText("");
            manuTextField.setText("");
            availSeatsTextField.setText("");
            planeTypeTextField.setText("");
            modelNoTextField.setText("");
            destinationTextField.setText("");
            startTextField.setText("");
            airlinerTextField.setText("");
            //licenseTextField.setText(""); 
            licenseCheckBox.setSelected(false);
            serialTextField.setText("");

        }
    }//GEN-LAST:event_createBtnActionPerformed

