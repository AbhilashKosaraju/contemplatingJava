    private void availSeatsTextFieldKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_availSeatsTextFieldKeyPressed
        // TODO add your handling code here:
        if (availSeatsTextField.getText().length() <= 2) {
            evt.consume();
        } else {
            availSeatsTextField.setEditable(false);
            JOptionPane.showMessageDialog(null, "Maximum allowed seats are 300");
            availSeatsTextField.setText("");
        }
        int key = evt.getKeyCode();
        if ((key >= evt.VK_NUMPAD0 && key <= evt.VK_NUMPAD9) || (key >= KeyEvent.VK_0 && key <= evt.VK_9) || (key == evt.VK_BACK_SPACE)) {
            availSeatsTextField.setEditable(true);
        } else {
            availSeatsTextField.setEditable(false);
            JOptionPane.showMessageDialog(null, " Input format should be in numbers");
            availSeatsTextField.setText("");

        }
    }//GEN-LAST:event_availSeatsTextFieldKeyPressed

