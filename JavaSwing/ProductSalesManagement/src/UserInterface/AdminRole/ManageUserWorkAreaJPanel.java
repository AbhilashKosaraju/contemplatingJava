    private void btnUpdateUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateUserActionPerformed
        int selectedRow = tblUser.getSelectedRow();
        if (selectedRow >= 0) {
            User user = (User) tblUser.getValueAt(selectedRow, 1);
            user.setActionFlag(1);
            AVUUserJPanel panel = new AVUUserJPanel(workAreaJPanel, business, user);
            workAreaJPanel.add("AVUUserJPanel", panel);
            CardLayout layout = (CardLayout) workAreaJPanel.getLayout();
            layout.next(workAreaJPanel);
        } else {
            JOptionPane.showMessageDialog(null, "Please select a user from the table first.", "Message", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnUpdateUserActionPerformed

