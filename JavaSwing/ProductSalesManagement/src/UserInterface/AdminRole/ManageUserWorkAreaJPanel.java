    public void populateUserTable() {
        DefaultTableModel dtm = (DefaultTableModel) tblUser.getModel();
        dtm.setRowCount(0);
        for (User u : business.getUserDirectory().getUserDirectory()) {
            Object row[] = new Object[5];
            row[0] = u.getUserId();
            row[1] = u;
            row[2] = u.getDepartment();
            row[3] = u.getRole();
            if (u.isActive()) {
                row[4] = "Active";
            }
            if (!u.isActive()) {
                row[4] = "Disabled";
            }
            dtm.addRow(row);
        }
    }

