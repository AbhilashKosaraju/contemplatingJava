    public void populateTableFromList(ArrayList<Person> alist) {
        DefaultTableModel dtm = (DefaultTableModel) tblCustomer.getModel();
        dtm.setRowCount(0);
        for (Person u : alist) {
            Object row[] = new Object[4];
            row[0] = u.getPersonId();
            row[1] = u;
            row[2] = u.getFirstName();
            row[3] = u.getLastName();
            dtm.addRow(row);
        }
    }

