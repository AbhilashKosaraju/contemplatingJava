    public void populatePersonTable() {
        DefaultTableModel dtm = (DefaultTableModel) tblCustomer.getModel();
        dtm.setRowCount(0);
        for (Person u : personDirectory.getPersonDirectory()) {
            Object row[] = new Object[4];
            row[0] = u.getPersonId();
            row[1] = u;
            row[2] = u.getLastName();
            row[3] = u.getAddress();
            dtm.addRow(row);
        }
    }

