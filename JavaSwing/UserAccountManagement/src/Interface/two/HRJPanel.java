    private void loginBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginBtnActionPerformed

        String userId = userIdTextField.getText();
        String pwd = pwdTextField.getText();

        for (Person p : personDirectory.getPersonDirectory()) {

            if (p.getUserName().equals(userId)) {
                if (p.getPassword().equals(pwd)) {
                    ViewDetailsJPanel panel = new ViewDetailsJPanel(userProcessContainer, personDirectory, p);
                    userProcessContainer.add("PersonDetailsWorkArea", panel);
                    CardLayout layout = (CardLayout) userProcessContainer.getLayout();
                    layout.next(userProcessContainer);
                } else {
                    JOptionPane.showMessageDialog(null, "Please enter the valid Password ");

                }

            }
        }


    }//GEN-LAST:event_loginBtnActionPerformed

