    private void addStateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addStateButtonActionPerformed
        // TODO add your handling code here:
        if (!stateNameJTextField.getText().equals("")) {
            String name = stateNameJTextField.getText();

            State state = network.getStateDirectory().addState();
            state.setName(name);

            populateStateTable();
            stateNameJTextField.setText("");
        } else {
            JOptionPane.showMessageDialog(null, "Please enter Country name");
        }
    }//GEN-LAST:event_addStateButtonActionPerformed

    private void stateNameJTextFieldKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_stateNameJTextFieldKeyPressed
        // TODO add your handling code here:
        int key = evt.getKeyCode();
        if ((key >= KeyEvent.VK_A && key <= KeyEvent.VK_Z) || (key == KeyEvent.VK_CAPS_LOCK) || (key == KeyEvent.VK_BACK_SPACE) || (key == KeyEvent.VK_SHIFT) || (key == KeyEvent.VK_SPACE)) {
            stateNameJTextField.setEditable(true);
        } else {
            stateNameJTextField.setEditable(false);
            JOptionPane.showMessageDialog(null, "Only alphabets are allowed");
            stateNameJTextField.setText("");
        }
    }//GEN-LAST:event_stateNameJTextFieldKeyPressed

    private void manageCitiesButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_manageCitiesButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = stateJTable.getSelectedRow();

        if (selectedRow < 0) {
            //return;
            JOptionPane.showMessageDialog(null, "Please select a country");
        } else {
            State state = (State) stateJTable.getValueAt(selectedRow, 0);
            ManageCitiesJPanel manageCitiesJPanel = new ManageCitiesJPanel(userProcessContainer, state);
            userProcessContainer.add("manageCitiesJPanel", manageCitiesJPanel);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
    }//GEN-LAST:event_manageCitiesButtonActionPerformed

    private void btnBakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBakActionPerformed
        // TODO add your handling code here:
        userProcessContainer.remove(this);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.previous(userProcessContainer);
    }//GEN-LAST:event_btnBakActionPerformed

    
    public void populateStateTable(){
        DefaultTableModel model = (DefaultTableModel) stateJTable.getModel();

        model.setRowCount(0);
        for (State state : network.getStateDirectory().getStateList()) {
            Object[] row = new Object[1];
            row[0] = state;
            model.addRow(row);
        }
    }

